class InvaderTestCase(unittest.TestCase):
    def setUp(self):
        self.tracker = InvaderTracker(RADAR, INVADER)
        # 2 invaders pattern found
        self.found_invader_locations = [[(1, 0, 11),
                                         (2, 0, 11),
                                         (3, 0, 11),
                                         (4, 0, 11),
                                         (5, 0, 11),
                                         (6, 0, 11),
                                         (7, 0, 11),
                                         (8, 0, 11)],
                                        [(12, 21, 32),
                                         (13, 21, 32),
                                         (14, 21, 32),
                                         (15, 21, 32),
                                         (16, 21, 32),
                                         (17, 21, 32),
                                         (18, 21, 32),
                                         (19, 21, 32)]]

    def test_search_invaders(self):
        self.tracker.search_invaders()
        self.assertEqual(len(self.tracker.invader_locations), 2)

    def test_invader_locations(self):
        self.tracker.search_invaders()
        self.assertEqual(self.tracker.invader_locations, self.found_invader_locations)


if __name__ == '__main__':
    unittest.main()
